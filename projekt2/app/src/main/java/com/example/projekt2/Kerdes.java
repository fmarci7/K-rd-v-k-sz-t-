package com.example.projekt2;

public class Kerdes {
    private String kerdes;
    private String tipus;

    public Kerdes(){}

    public Kerdes(String kerdes, String tipus) {
        this.kerdes = kerdes;
        this.tipus = tipus;
    }

    public String getKerdes() {
        return kerdes;
    }

    public String getTipus() {
        return tipus;
    }
}
